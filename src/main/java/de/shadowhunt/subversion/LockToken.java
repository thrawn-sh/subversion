package de.shadowhunt.subversion;

import javax.annotation.concurrent.Immutable;

import org.apache.commons.lang3.Validate;

/**
 * {@link LockToken} holds key for a {@link Resource}, that has been marked by an user.
 */
@Immutable
public final class LockToken {

    private final String token;

    public LockToken(final String token) {
        Validate.notBlank(token, "token must not be null or blank");
        this.token = token;
    }

    @Override
    public boolean equals(final Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final LockToken other = (LockToken) obj;
        if (token == null) {
            if (other.token != null) {
                return false;
            }
        } else if (!token.equals(other.token)) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = (prime * result) + ((token == null) ? 0 : token.hashCode());
        return result;
    }

    @Override
    public String toString() {
        return token;
    }
}
